package common.dto;

public class Player {
	private String position;
	private String uniformNumber;
	private String nameKanji;
	private String nameKana;
	private String birthplace;
	private String birthday;
	private String height;
	private String weight;
	private String bloodType;
	private String dominantHand;
	private String dominantdrop;
	private String draftYear;
	private String draftRank;

	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getUniformNumber() {
		return uniformNumber;
	}
	public void setUniformNumber(String uniformNumber) {
		this.uniformNumber = uniformNumber;
	}
	public String getNameKanji() {
		return nameKanji;
	}
	public void setNameKanji(String nameKanji) {
		this.nameKanji = nameKanji;
	}
	public String getNameKana() {
		return nameKana;
	}
	public void setNameKana(String nameKana) {
		this.nameKana = nameKana;
	}
	public String getBirthplace() {
		return birthplace;
	}
	public void setBirthplace(String birthplace) {
		this.birthplace = birthplace;
	}
	public String getBirthday() {
		return birthday;
	}
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getBloodType() {
		return bloodType;
	}
	public void setBloodType(String bloodType) {
		this.bloodType = bloodType;
	}
	public String getDominantHand() {
		return dominantHand;
	}
	public void setDominantHand(String dominantHand) {
		this.dominantHand = dominantHand;
	}
	public String getDominantdrop() {
		return dominantdrop;
	}
	public void setDominantdrop(String dominantdrop) {
		this.dominantdrop = dominantdrop;
	}
	public String getDraftYear() {
		return draftYear;
	}
	public void setDraftYear(String draftYear) {
		this.draftYear = draftYear;
	}
	public String getDraftRank() {
		return draftRank;
	}
	public void setDraftRank(String draftRank) {
		this.draftRank = draftRank;
	}
}
